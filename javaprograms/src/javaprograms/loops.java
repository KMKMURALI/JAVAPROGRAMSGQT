package javaprograms;

public class loops {

	public static void main(String[] args) {
		// For loop
		for(int a =1;a<=15;a++) {
			System.out.println("msd");
		}
		int b = 10;
		while(b<=1) {
			System.out.println("nnn");
			b--;
		}
		// do while
		int k =1;
		do {
			System.out.println("jjj");
			k++;
			
		}
		while(k<=10);
	}
}
